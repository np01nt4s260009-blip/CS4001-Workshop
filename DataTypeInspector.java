public class DataTypeInspector {
    public static void main(String[] args) {
        //Declaring all 8 types of data types
        byte age = 30;
        short year = 2023;
        int id = 214752;
        long population = 10000000L;
        float price = 3.4F;
        double pi = 3.44; 
        char grade = 'A';
        boolean fig  = true;
        System.out.println("byte value" + age);
        System.out.println("short value" + year);
        System.out.println("int value" + id);
        System.out.println("long value" + population);
        System.out.println("float value" + price);
        System.out.println("double value" + pi);
        System.out.println("char valu" + grade);
        System.out.println("boolean value" + fig);
    }
}